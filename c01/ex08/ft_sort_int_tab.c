#include <unistd.h>

void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	min_index;
	int	tmp;

	i = 0;
	while (i < size - 1)
	{
		min_index = i;
		j = i + 1;
		while (j < size)
		{
			if (tab[j] < tab[min_index])
			{
				min_index = j;
			}
			j++;
		}
		tmp = tab[i];
		tab[i] = tab[min_index];
		tab[min_index] = tmp;
		i++;
	}
}
