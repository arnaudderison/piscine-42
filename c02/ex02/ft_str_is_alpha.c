#include <unistd.h>

int	ft_str_is_alpha(char *str)
{
	if (str == NULL)
		return ('\0');
	while (*str != '\0')
	{
		if ((*str >= 'a' && *str <= 'z') || (*str >= 'A' && *str <= 'Z'))
			str++;
		else
			return (0);
	}
	return (1);
}
