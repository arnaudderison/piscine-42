int	ft_any(char **tab, int (*f)(char *))
{
	int	i;

	if (!tab || !f)
		return (0);
	i = -1;
	while (tab[++i])
	{
		if (f(tab[i]) != 0)
			return (1);
	}
	return (0);
}
